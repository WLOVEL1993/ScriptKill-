package com.hualife.scriptkill.model;

public class Script {
    private Integer scriptId;

    private String scriptName;

    private String scriptDifficulty;

    private String scriptType;

    private Integer scriptPopulation;

    private String scriptState;

    private String scriptBrief;

    private String scriptResult;

    public Integer getScriptId() {
        return scriptId;
    }

    public void setScriptId(Integer scriptId) {
        this.scriptId = scriptId;
    }

    public String getScriptName() {
        return scriptName;
    }

    public void setScriptName(String scriptName) {
        this.scriptName = scriptName;
    }

    public String getScriptDifficulty() {
        return scriptDifficulty;
    }

    public void setScriptDifficulty(String scriptDifficulty) {
        this.scriptDifficulty = scriptDifficulty;
    }

    public String getScriptType() {
        return scriptType;
    }

    public void setScriptType(String scriptType) {
        this.scriptType = scriptType;
    }

    public Integer getScriptPopulation() {
        return scriptPopulation;
    }

    public void setScriptPopulation(Integer scriptPopulation) {
        this.scriptPopulation = scriptPopulation;
    }

    public String getScriptState() {
        return scriptState;
    }

    public void setScriptState(String scriptState) {
        this.scriptState = scriptState;
    }

    public String getScriptBrief() {
        return scriptBrief;
    }

    public void setScriptBrief(String scriptBrief) {
        this.scriptBrief = scriptBrief;
    }

    public String getScriptResult() {
        return scriptResult;
    }

    public void setScriptResult(String scriptResult) {
        this.scriptResult = scriptResult;
    }
}